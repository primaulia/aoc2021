describe '#process' do
  let(:input) {
    "0,9 -> 5,9
    8,0 -> 0,8
    9,4 -> 3,4
    2,2 -> 2,1
    7,0 -> 7,4
    6,4 -> 2,0
    0,9 -> 2,9
    3,4 -> 1,4
    0,0 -> 8,8
    5,5 -> 8,2"
  }
  
  let(:output) {
    [
      {
        x: [0, 5],
        y: [9, 9]
      },
      {
        x: [8, 0],
        y: [0, 8]
      },
      {
        x: [9, 3],
        y: [4, 4]
      },
      {
        x: [2, 2],
        y: [2, 1]
      },
      {
        x: [7, 7],
        y: [0, 4]
      },
      {
        x: [6, 2],
        y: [4, 0]
      },
      {
        x: [0, 2],
        y: [9, 9]
      },
      {
        x: [3, 1],
        y: [4, 4]
      },
      {
        x: [0, 8],
        y: [0, 8]
      },
      {
        x: [5, 8],
        y: [5, 2]
      }
    ]
  }
  
  it 'should convert all input to an array of x and y' do
    expect(process(input)).to eq(output)    
  end
end

